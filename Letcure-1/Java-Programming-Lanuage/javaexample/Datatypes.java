// DataType in  Java

public class Datatypes {

    public static void main(String[] args) {
        byte b = 8;
        System.out.println(b);
        char ch = 'a';
        System.out.println(ch);
        boolean var = true;
        System.out.println(var);
        float price = 10.5f;
        int num = 89;
        System.out.println(price);
        System.out.println(num);
    }
}

// Sum of Number
class SumofTwo {

    public static void main(String[] args) {
        int a = 10;
        int b = 50;
        int sum = a + b;
        System.out.println(sum);
    }
}

